public class Tester {
    private String ter_code;
    private String ter_name;
    private String ter_no;
    private String ter_model;
    private String ter_kind;
    private String ter_man;
    private String ter_bigo;

    public String getTer_code()  { return ter_code; }
    public void setTer_code(String v)  { this.ter_code = v; }
    public String getTer_name()  { return ter_name; }
    public void setTer_name(String v)  { this.ter_name = v; }
    public String getTer_no()    { return ter_no; }
    public void setTer_no(String v)    { this.ter_no = v; }
    public String getTer_model() { return ter_model; }
    public void setTer_model(String v) { this.ter_model = v; }
    public String getTer_kind()  { return ter_kind; }
    public void setTer_kind(String v)  { this.ter_kind = v; }
    public String getTer_man()   { return ter_man; }
    public void setTer_man(String v)   { this.ter_man = v; }
    public String getTer_bigo()  { return ter_bigo; }
    public void setTer_bigo(String v)  { this.ter_bigo = v; }
}
